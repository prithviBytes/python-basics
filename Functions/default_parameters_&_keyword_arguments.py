def full_name(first_name = "Prithvi",last_name = "Suvarna"): #If no arguments are passed this would be the default value
    print(f"Your name is {first_name} {last_name}")

full_name("Prithvi","Suvarna") #Your name is Prithvi Suvarna

full_name(last_name = "Suvarna", first_name = "Prithvi") #Your name is Prithvi Suvarna

